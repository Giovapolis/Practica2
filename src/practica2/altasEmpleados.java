    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed
        Connection conexion = lanzador.conexion.conectado();
        String sql = "INSERT INTO empleado (nomEmpresa,fechaIngreso,NOMBRE,APATERNO,AMATERNO,EDAD,TEL,CEL,CURP,RFC,correo,cargo,CALLE,"
                + "NUM_INT,NUM_EXT,COLONIA,MUNICIPIO,ESTADO) VALUES  (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
        try {
               PreparedStatement pst = conexion.prepareStatement(sql);
               pst.setString(1, nomEmp.getText() );
               pst.setDate(2, Date.valueOf(fecha.getText()));
               pst.setString(3, nombre.getText());
               pst.setString(4, apMaterno.getText());
               pst.setString(5, apPaterno.getText());
               pst.setInt(6, Integer.parseInt(edad.getText()));
               pst.setInt(7, Integer.parseInt(telFijo.getText()));
               pst.setInt(8, Integer.parseInt(telCel.getText()));
               pst.setString(9, curp.getText());
               pst.setString(10, rfc.getText());
               pst.setString(11, email.getText());
               pst.setString(12, cargo.getText());
               pst.setString(13, calle.getText());
               pst.setString(14, numInt.getText());
               pst.setString(15, numExt.getText());
               pst.setString(16, colonia.getText());
               pst.setString(17, municipio.getText());
               pst.setString(18, estado.getText());
               if ( pst.executeUpdate() > 0 ){
                    JOptionPane.showMessageDialog(null, "Datos Ingresados");
               }
          } catch (SQLException ex) {
               Logger.getLogger(altasEmpleados.class.getName()).log(Level.SEVERE, null, ex);
               JOptionPane.showMessageDialog(null, "Datos no Ingresados");
               JOptionPane.showMessageDialog(null, "Llena todos los datos");
          }
    }//GEN-LAST:event_btnRegistroActionPerformed

